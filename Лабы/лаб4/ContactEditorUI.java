    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.setDialogTitle("Выбор директории");
        if (fc.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            try {
                FileInputStream fis = new FileInputStream(fc.getSelectedFile());
                BufferedInputStream bis = new BufferedInputStream(fis);
                ObjectInputStream is = new ObjectInputStream(bis);
                LinkedList<RecIntegral> myList = (LinkedList<RecIntegral>) is.readObject();
                list = myList;
                for (int i = 0; i < list.size(); i++) {
                    jTable1.setValueAt(list.get(i).topBorder, i, 0);
                    jTable1.setValueAt(list.get(i).downBorder, i, 1);
                    jTable1.setValueAt(list.get(i).step, i, 2);
                }
                is.close();
            } catch (Exception e) {
                System.out.println(e.getMessage());

            }
        }
    }//GEN-LAST:event_jButton9ActionPerformed


