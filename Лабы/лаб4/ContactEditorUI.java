    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel myTable = (DefaultTableModel) jTable1.getModel();
        int rowCount = myTable.getRowCount();
        for (int i = 0; i < rowCount; i++) {
//            try {
            double topBorder, downBorder, step;
            topBorder = Double.parseDouble(myTable.getValueAt(i, 0).toString());
            downBorder = Double.parseDouble(myTable.getValueAt(i, 1).toString());
            step = Double.parseDouble(myTable.getValueAt(i, 2).toString());
//            RecIntegral ri = new RecIntegral();
//            ri.setStep(step);
            if ((downBorder > topBorder) || (downBorder == 0) || (topBorder == 0) || (step == 0)) {
                return;
            }
            if ((downBorder < 0) && (topBorder > 0)) {
                myTable.setValueAt("Infinity", i, 3);
            } else {
                double nextStep = downBorder;
                double prevStep = downBorder;
                double result = 0;
                do {
                    nextStep = nextStep + step;
                    if (nextStep > topBorder) {
                        nextStep = topBorder;
                    }
//                    ri.setIterationResult(iterationResult);
                    double iterationResult = f(nextStep) + f(prevStep);

                    iterationResult = iterationResult * (nextStep - prevStep) / 2;
                    result += iterationResult;
                    prevStep = nextStep;

                } while (nextStep != topBorder);
                myTable.setValueAt(result, i, 3);
            }
//            } catch (ExceptionLab3 ex) {
//                        JOptionPane.showMessageDialog(null, ex.getMessage(), "Ошибка", JOptionPane.ERROR_MESSAGE);
//            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

